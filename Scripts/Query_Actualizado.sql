CREATE FUNCTION [dbo].[fn_tipo_cliente] (@id_rango INT)
RETURNS VARCHAR(20)
AS
BEGIN
    DECLARE @tipo VARCHAR(20);
    SELECT @tipo = DESCRIPCION FROM RANGO_SALARIAL WHERE PK_ID_RANGO = @id_rango;
